  status,
  COUNT(*) as count,
  SUM(value) as total_value,
  AVG(value) as avg_value
FROM projects
GROUP BY status
ORDER BY 
  CASE status
    WHEN 'Bidding' THEN 1
    WHEN 'Submitted' THEN 2
    WHEN 'Awarded' THEN 3
    WHEN 'In Progress' THEN 4
    WHEN 'Lost' THEN 5
